def get_mc4(nsamples, seed, seqlen, model, lang="de"):
    """
    Multilingual C4 (mC4) loader.
    name format in get_loaders: mc4-<lang>, e.g. mc4-de, mc4-en, mc4-tr
    Uses streaming to avoid huge downloads.
    """
    from datasets import load_dataset
    from transformers import AutoTokenizer
    import random
    import torch

    tokenizer = AutoTokenizer.from_pretrained(model, use_fast=False)

    # streaming to avoid downloading the full dataset
    traindata = load_dataset("mc4", lang, split="train", streaming=True)
    valdata   = load_dataset("mc4", lang, split="validation", streaming=True)

    random.seed(seed)

    # ---- Calibration samples (nsamples) ----
    trainloader = []
    token_buffer = []

    for ex in traindata:
        text = ex.get("text", "")
        if not text:
            continue

        ids = tokenizer(text, return_tensors="pt", truncation=False).input_ids[0]
        token_buffer.extend(ids.tolist())

        while len(token_buffer) >= seqlen + 1 and len(trainloader) < nsamples:
            chunk = token_buffer[:seqlen]
            token_buffer = token_buffer[seqlen:]

            inp = torch.tensor(chunk, dtype=torch.long).unsqueeze(0)
            tar = inp.clone()
            tar[:, :-1] = -100
            trainloader.append((inp, tar))

        if len(trainloader) >= nsamples:
            break

    # ---- Evaluation tokens (build ~256 sequences) ----
    random.seed(0)
    valenc_chunks = []
    token_buffer = []

    for ex in valdata:
        text = ex.get("text", "")
        if not text:
            continue

        ids = tokenizer(text, return_tensors="pt", truncation=False).input_ids[0]
        token_buffer.extend(ids.tolist())

        while len(token_buffer) >= seqlen and len(valenc_chunks) < 256:
            chunk = token_buffer[:seqlen]
            token_buffer = token_buffer[seqlen:]
            valenc_chunks.append(torch.tensor(chunk, dtype=torch.long).unsqueeze(0))

        if len(valenc_chunks) >= 256:
            break

    if len(valenc_chunks) == 0:
        raise RuntimeError(f"Could not build validation set for mc4-{lang}. Try smaller seqlen or different lang.")

    valenc = torch.hstack(valenc_chunks)

    class TokenizerWrapper:
        def __init__(self, input_ids):
            self.input_ids = input_ids

    return trainloader, TokenizerWrapper(valenc)


def get_loaders(name, nsamples=128, seed=0, seqlen=2048, model=""):
    if 'wikitext2' in name:
        return get_wikitext2(nsamples, seed, seqlen, model)

    if 'ptb' in name:
        if 'new' in name:
            return get_ptb_new(nsamples, seed, seqlen, model)
        return get_ptb(nsamples, seed, seqlen, model)

    if 'mc4' in name:
        parts = name.split('-')
        lang = parts[1] if len(parts) > 1 else "de"
        return get_mc4(nsamples, seed, seqlen, model, lang=lang)

    if 'c4' in name:
        if 'new' in name:
            return get_c4_new(nsamples, seed, seqlen, model)
        return get_c4(nsamples, seed, seqlen, model)

    raise ValueError(f"Unknown dataset: {name}")


